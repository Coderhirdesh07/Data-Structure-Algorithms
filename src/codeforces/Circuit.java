package codeforces;

import java.util.Arrays;
import java.util.Scanner;

public class Circuit {
    public static void main(String[] args) {
//       2096  A. Wonderful Sticks

        Scanner sc = new Scanner(System.in);
       int t = sc.nextInt();
       while(t-->0){
            int n = sc.nextInt();
            int S = sc.nextInt();
            int[] arr = new int[n];
            for(int i=0;i<n;i++){
                arr[i] = sc.nextInt();
            }
           Arrays.sort(arr);
            int max = Math.abs(arr[n-1] - arr[0]) + Math.min(Math.abs(arr[0]-S),Math.abs(arr[n-1]-S));
           System.out.println(max);

         }
        // 1 2 3 6 9 10 11
        // 0 1 1 3 3 1 1

        }

    }



